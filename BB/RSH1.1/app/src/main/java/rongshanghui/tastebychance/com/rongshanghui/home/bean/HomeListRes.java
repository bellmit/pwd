public class HomeListRes implements Serializable {

    private int id;//	文章id
    private String post_title;//	文章标题
    private String publish_location;//	文章发布位置
    private String post_hits;//	文章点击数（万）
    private String m_post_image1;//	缩略图路径
    private String detail;
    private int is_collect;// 	1 已收藏 0 未收藏
    private int is_like;// 	1 已点赞 0 未点赞

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPost_title() {
        return post_title;
    }

    public void setPost_title(String post_title) {
        this.post_title = post_title;
    }

    public String getPublish_location() {
        return publish_location;
    }

    public void setPublish_location(String publish_location) {
        this.publish_location = publish_location;
    }

    public String getPost_hits() {
        return post_hits;
    }

    public void setPost_hits(String post_hits) {
        this.post_hits = post_hits;
    }

    public String getM_post_image1() {
        return m_post_image1;
    }

    public void setM_post_image1(String m_post_image1) {
        this.m_post_image1 = m_post_image1;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public int getIs_collect() {
        return is_collect;
    }

    public void setIs_collect(int is_collect) {
        this.is_collect = is_collect;
    }

    public int getIs_like() {
        return is_like;
    }

    public void setIs_like(int is_like) {
        this.is_like = is_like;
    }
}
