public class SBManagerBean implements Serializable {

    /**
     * post_title 	string 	文章标题
     * is_read 	int 	0 未查看 1 已查看
     * id 	int 	文章对应id
     * detail 	string 	详情页链接
     * <p>
     * post_title : 文章标题
     * is_read : 1
     * id : 567
     * detail : http://rsh.com/index.php/api/html/detail/id/567.html
     */

    private String post_title;
    private int is_read;
    private int id;
    private String detail;

    public String getPost_title() {
        return post_title;
    }

    public void setPost_title(String post_title) {
        this.post_title = post_title;
    }

    public int getIs_read() {
        return is_read;
    }

    public void setIs_read(int is_read) {
        this.is_read = is_read;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
