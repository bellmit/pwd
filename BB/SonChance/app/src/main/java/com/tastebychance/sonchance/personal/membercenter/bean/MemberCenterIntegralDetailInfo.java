public class MemberCenterIntegralDetailInfo  implements Serializable {
    private int id	;//	记录ID号
    private int uid	;//	用户ID
    private int score	;//	用户积分增减
    private int type	;//	积分类型 0为负数，1为正数
    private String msg	;//	用户积分记录信息
    private Date create_time	;//	记录生存日期

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Date getCreate_time() {
        return create_time;
    }

    public void setCreate_time(Date create_time) {
        this.create_time = create_time;
    }
}
